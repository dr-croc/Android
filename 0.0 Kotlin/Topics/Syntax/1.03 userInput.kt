fun main(args: Array<String>) {

    var scanner = Scanner(System.`in`)

    var int = scanner.nextInt()
    println(int)
    println(int:: class)

    var dou = scanner.nextDouble()
    println(dou)
    println(dou:: class)

    var st = scanner.next()
    println(st)
    println(st::class)

    var boolean = scanner.nextBoolean()
    println(boolean::class)


}
